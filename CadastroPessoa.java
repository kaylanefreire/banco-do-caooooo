    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String cpf = jFormattedTextField1.getText();
        String nome = jTextField1.getText();
        int idade = Integer.parseInt(jTextField2.getText());
        
        Pessoa p = new Pessoa();
        p.setCpf(cpf);
        p.setNome(nome);
        p.setIdade(idade);
        
        ConexaoPessoa banco = new ConexaoPessoa();
        banco.inserir(p);
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        ConexaoPessoa banco = new ConexaoPessoa();
        ArrayList<Pessoa> lista = banco.consultar();
        modelo.setNumRows(0);
        for(Pessoa p : lista){
            modelo.addRow(new Object[]{p.getCpf(),p.getNome(),p.getIdade()});
        }
    }//GEN-LAST:event_jButton2ActionPerformed

